Materialize.BACKGROUND_COLORS.push("black", "white");

let lightenDarkenColors = _.each(["lighten", "darken", "accent"], type => {
  _.each(baseColors, color => {
    if (type === "accent" && _.includes(["brown", "grey", "blue-grey"], color)) {
      return;
    }

    _.times(type === "lighten" ? 5 : 4, n => {
      Materialize.BACKGROUND_COLORS.push(color + " " + type + "-" + (n + 1));
    });
  });
});

Materialize.TEXT_COLORS = _.map(Materialize.BACKGROUND_COLORS, color => {
  if (_.includes(color, " ")) {
    return color.replace(" ", "-text text-");
  } else {
    return color + "-text";
  }
});

